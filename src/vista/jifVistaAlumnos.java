    private void cargarTabla() {
        ArrayList<Alumno> listaAlumno = ad.obtenerAlumnos();
        
        modelo.setRowCount(0);

        for (Alumno a : listaAlumno) {
            Object[] fila = {
                a.getNombre(),
                a.getApellido(),
                a.getDni(),
                a.getFecha(),
                a.isEstado()
            };
            modelo.addRow(fila);
        }
    }
    
