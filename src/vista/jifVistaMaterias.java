    private void cargarTabla() {
        modeloTabla.setRowCount(0);
        try {
            List<Materia> materias = materiaData.listarMaterias();
            for (Materia m : materias) {
                modeloTabla.addRow(new Object[]{
                    m.getIdMateria(),
                    m.getNombre(),
                    m.getAnio(),
                    m.isEstado() ? "Activo" : "Inactivo"
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar materias: " + e.getMessage());
        }
    }
    
     private void limpiarCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTable1.clearSelection();
    }

     private void crearMateria() {
        try {
            String nombre = jTextField2.getText();
            int anio = Integer.parseInt(jTextField3.getText());
            boolean estado = jTextField4.getText().equalsIgnoreCase("activo");

            Materia m = new Materia(nombre, anio, estado);
            materiaData.insertarMateria(m);
            JOptionPane.showMessageDialog(this, "Materia creada correctamente.");
            cargarTabla();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al crear materia: " + e.getMessage());
        }
    }
     
     private void actualizarMateria() {
        try {
            int id = Integer.parseInt(jTextField1.getText());
            String nombre = jTextField2.getText();
            int anio = Integer.parseInt(jTextField3.getText());
            materiaData.actualizarMateria(id, nombre, anio);
            JOptionPane.showMessageDialog(this, "Materia actualizada correctamente.");
            cargarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar materia: " + e.getMessage());
        }
    }
     
     private void eliminarMateria() {
        try {
            int id = Integer.parseInt(jTextField1.getText());
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Â¿Seguro que deseas eliminar esta materia?", "Confirmar",
                    JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                materiaData.borrarMateria(id);
                JOptionPane.showMessageDialog(this, "Materia eliminada correctamente.");
                cargarTabla();
                limpiarCampos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar materia: " + e.getMessage());
        }
    }
     
